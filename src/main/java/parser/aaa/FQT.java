package parser.aaa;

public class FQT {
    private char op;
    private char leftdata;
    private char rightdata;
    private char result;

    public char getOp() {
        return op;
    }

    public void setOp(char op) {
        this.op = op;
    }

    public char getLeftdata() {
        return leftdata;
    }

    public void setLeftdata(char leftdata) {
        this.leftdata = leftdata;
    }

    public char getRightdata() {
        return rightdata;
    }

    public void setRightdata(char rightdata) {
        this.rightdata = rightdata;
    }

    public char getResult() {
        return result;
    }

    public void setResult(char result) {
        this.result = result;
    }

}
